#a função set remove duplicados
numeros = set([1, 2, 3, 1, 3, 4])
print(numeros)  # {1, 2, 3, 4}

letras = set("abacaxi")
print(letras)  # {"b", "a", "c", "x", "i"}

carros = set(("palio", "gol", "celta", "palio"))
print(carros)  # {"gol", "celta", "palio"}

#outra forma de criar set (remove duplicados)
linguagens = {"csharp", "java", "csharp"}
print(linguagens)